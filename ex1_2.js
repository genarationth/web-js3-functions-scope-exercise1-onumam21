// ฟังก์ชัน inform ที่รับพารามิเตอร์สามตัวและแสดงข้อความตามที่กำหนด
function inform(firstName, location, hobby) {
    return "Hi, my name is " + firstName + ". I live in " + location + " and enjoy " + hobby + ".";
  }
  
  // เรียกใช้งานฟังก์ชัน inform และแสดงผลลัพธ์ด้วย console.log
  console.log(inform("John", "New York", "reading"));
  // Output: Hi, my name is John. I live in New York and enjoy reading.
  
  console.log(inform("Jane", "Los Angeles", "hiking"));
  // Output: Hi, my name is Jane. I live in Los Angeles and enjoy hiking.
  